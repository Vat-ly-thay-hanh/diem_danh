/******************************************************************************
 * layout.js
 * ---------------------------------------------------------------------------
 * Mô tả toàn bộ sơ đồ lớp.
 *
 * Quy ước:
 *  - Chuỗi "1A", "1B"... là một vị trí ngồi.
 *  - null là lối đi.
 *
 * Không có xử lý giao diện trong file này.
 ******************************************************************************/

export const CLASS_LAYOUT = [

    // Hàng trên cùng (gần bảng)
    ["10A","10B","10C","10D", null ,"10E","10F","10G","10H"],

    ["9A","9B","9C","9D", null ,"9E","9F","9G","9H"],

    ["8A","8B","8C","8D", null ,"8E","8F","8G","8H"],

    ["7A","7B","7C","7D", null ,"7E","7F","7G","7H"],

    ["6A","6B","6C","6D", null ,"6E","6F","6G","6H"],

    ["5A","5B","5C","5D", null ,"5E","5F","5G","5H"],

    ["4A","4B","4C","4D", null ,"4E","4F","4G","4H"],

    ["3A","3B","3C","3D", null ,"3E","3F","3G","3H"],

    ["2A","2B","2C","2D", null ,"2E","2F","2G","2H"],

    // Hàng cuối lớp
    ["1A","1B","1C","1D", null ,"1E","1F","1G","1H"]

];

/******************************************************************************
 * Trả về danh sách toàn bộ vị trí ngồi.
 * Ví dụ:
 * [
 *   "10A","10B",...,"1H"
 * ]
 ******************************************************************************/

export function getAllSeatIds() {

    const result = [];

    for (const row of CLASS_LAYOUT) {

        for (const seat of row) {

            if (seat !== null) {

                result.push(seat);

            }

        }

    }

    return result;

}

/******************************************************************************
 * Kiểm tra có phải vị trí hợp lệ hay không.
 ******************************************************************************/

export function isValidSeat(id) {

    return getAllSeatIds().includes(id);

}

/******************************************************************************
 * Tìm vị trí trên sơ đồ.
 *
 * Trả về:
 * {
 *      row:0,
 *      col:3
 * }
 *
 * hoặc null.
 ******************************************************************************/

export function findSeat(id) {

    for (let r = 0; r < CLASS_LAYOUT.length; r++) {

        for (let c = 0; c < CLASS_LAYOUT[r].length; c++) {

            if (CLASS_LAYOUT[r][c] === id) {

                return {

                    row: r,

                    col: c

                };

            }

        }

    }

    return null;

}

/******************************************************************************
 * Tổng số ghế.
 ******************************************************************************/

export const TOTAL_SEATS = getAllSeatIds().length;

/******************************************************************************
 * Tổng số hàng.
 ******************************************************************************/

export const TOTAL_ROWS = CLASS_LAYOUT.length;

/******************************************************************************
 * Tổng số cột.
 ******************************************************************************/

export const TOTAL_COLUMNS = CLASS_LAYOUT[0].length;